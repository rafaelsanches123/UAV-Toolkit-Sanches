package uav.mission_creator.converter.map;

/**
 *
 * @author Jesimar S. Arantes
 */
public class Point2D {
    
    private final double x;
    private final double y;

    public Point2D(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
    
}
